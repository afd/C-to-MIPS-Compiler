int add(int add_a, int add_b){
	return add_a + add_b;
}
int sub(int sub_a, int sub_b){
	return sub_a - sub_b;
}
int mul(int mul_a, int mul_b){
	return mul_a * mul_b;
}
int div(int div_a, int div_b){
	return div_a / div_b;
}
int mod(int mod_a, int mod_b){
	return mod_a % mod_b;
}
int shr(int shr_a, int shr_b){
	return shr_a >> shr_b;
}
int shl(int shl_a, int shl_b){
	return shl_a << shl_b;
}
int band(int band_a, int band_b){
	return band_a & band_b;
}
int bor(int bor_a, int bor_b){
	return bor_a | bor_b;
}
int bxor(int bxor_a, int bxor_b){
	return bxor_a ^ bxor_b;
}
