int add(int a, int b){
	return a + b;
}
int sub(int a, int b){
	return a - b;
}
int mul(int a, int b){
	return a * b;
}
int div(int a, int b){
	return a / b;
}
int mod(int a, int b){
	return a % b;
}
int shr(int a, int b){
	return a >> b;
}
int shl(int a, int b){
	return a << b;
}
int band(int a, int b){
	return a & b;
}
int bor(int a, int b){
	return a | b;
}
int bxor(int a, int b){
	return a ^ b;
}
